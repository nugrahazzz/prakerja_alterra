// Tuliskan Command-command yang ada pada Flutter CLI

// flutter create <nama_proyek>
// Tujuannya untuk membuat proyek Flutter baru dengan nama yang diinputkan. Ini akan menyiapkan semua file dan folder yang dibutuhkan untuk memulai membuat aplikasi.

// flutter run
// Tujuannya untuk menjalankan aplikasi Flutter di emulator atau perangkat yang terhubung ke komputer. Jika belum ada perangkat yang tersedia.

// flutter build <target>
// Tujuannya membuat versi aplikasi untuk platform tertentu. Misalnya, flutter build apk akan menghasilkan file APK untuk Android, sementara flutter build ios untuk iOS.

// flutter doctor
// Tujuannya untuk memeriksa apakah semua tools yang diperlukan untuk pengembangan Flutter sudah terinstal dan berfungsi dengan baik. Ini akan membantu melihat apakah ada yang perlu diperbaiki atau tidak.

// flutter pub get
// Tujuannya mengunduh dan pasang paket yang sudah didefinisikan dalam file pubspec.yaml.

// flutter pub upgrade
// Tujuannya memperbarui paket-paket di proyek ke versi terbaru yang tersedia. Berguna untuk mendapatkan fitur terbaru atau perbaikan bug dari paket yang digunakan.

// flutter analyze
// Tujuannya untuk memeriksa kode Dart untuk mencari masalah atau kesalahan, menjaga kualitas kode dan menemukan bug lebih awal.

// flutter test
// Tujuannya menjalankan tes untuk memastikan aplikasi berfungsi dengan benar. Ini akan memeriksa bagian-bagian tertentu dari aplikasi untuk memastikan semuanya bekerja sesuai yang diharapkan.

// flutter clean
// Tujuannya memersihkan file build dan file sementara dari proyek. Ini membantu mengatasi masalah build dan memastikan proyek bersih dari file yang tidak perlu.

// flutter packages pub global activate <package>
// Tujuannya untuk mengaktifkan paket global yang memungkinkan pengguna menggunakan alat dan skrip Dart di sistem secara umum. Misalnya, mengaktifkan alat webdev untuk pengembangan web.

// flutter devices
// Tujuannya untuk menampilkan daftar perangkat yang tersedia untuk menjalankan aplikasi, termasuk emulator dan perangkat fisik yang terhubung ke komputer.

// flutter emulators
// Tujuannya Tampilkan dan kelola emulator. Melihat daftar emulator yang ada, dan juga membuat atau memulai emulator dari sini.

// flutter pub outdated
// Tujuannya Cek paket yang sudah usang dan lihat versi terbaru yang tersedia. Berguna untuk memastikan dependensi proyek tetap terbaru.

// flutter format <file/directory>
// Tujuannya Format kode Dart dalam file atau folder tertentu supaya mengikuti standar gaya yang konsisten. Ini membantu menjaga tampilan kode tetap rapi.

// flutter config
// Tujuannya Atur berbagai pengaturan Flutter, seperti pengaturan proxy atau saluran rilis.